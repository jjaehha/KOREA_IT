package day03;

public class BitOper {
	public static void main(String[] args) {
		System.out.println(10&11);
		System.out.println(10|11);
		System.out.println(10^11);
		System.out.println(~10);	// a >>> -a-1
		
		System.out.println("===================");
		System.out.println(10>>1);
		System.out.println(10>>2);
	}

}
